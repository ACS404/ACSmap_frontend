  // ─── Key bindings ────────────────────────────────────────────────────────
  function attachKeys(game) {
    // Use the canvas parent as focus target so we don't break page scroll
    document.addEventListener('keydown', e => {
      if (!game.running) return;
      game.keys[e.code] = true;
      if (e.code === 'Space') { e.preventDefault(); tryRepair(game); }
      if (['ArrowUp','ArrowDown','ArrowLeft','ArrowRight'].includes(e.code)) e.preventDefault();
    });
    document.addEventListener('keyup', e => { game.keys[e.code] = false; });
  }

  // ─── Reset phase ─────────────────────────────────────────────────────────
  function resetPhase(game, phaseIdx) {
    const W = game.canvas.width, H = game.canvas.height;
    game.phase = phaseIdx;
    game.checkpointPassed = false;
    game.checkpointOpen   = false;
    game.nutrients        = 0;
    game.nutrientsNeeded  = 4 + phaseIdx * 2;
    game.repairCooldown   = 0;
    game.player.x = 60;
    game.player.y = H / 2;
    game.entities = [];
    game.walls    = [];
    generateEntities(game);
    generateWalls(game);
    game.running = true;
  }

  function resetAll(game, uiEl, overlayEl) {
    Object.assign(game, {
      phase:0, dna:100, damage:0, repairCooldown:0, repairCharges:3, score:0,
      nutrients:0, nutrientsNeeded:5, checkpointOpen:false, checkpointPassed:false,
      gameOver:false, particles:[], floatTexts:[], p53Active:false, p53Timer:0,
      frameCount:0, running:false,
    });
    hideOverlay(overlayEl);
    resetPhase(game, 0);
    updateUI(game, uiEl);
    loop(game, uiEl, overlayEl);
  }

  // ─── Entity generation ───────────────────────────────────────────────────
  function generateEntities(game) {
    const W = game.canvas.width, H = game.canvas.height;
    const p = game.phase;

    const mutagens = MUTAGEN_INFO.map(m => ({
      type:'mutagen', label:m.label, color:m.color, size:13, dmg:m.dmg,
      x:0,y:0,vx:0,vy:0,angle:0,hit:false
    }));
    const nutrients = [
      {type:'nutrient',color:C.sage,  label:'N',size:10,bob:0,collected:false},
      {type:'nutrient',color:C.tealGame,label:'A',size:10,bob:0,collected:false},
      {type:'nutrient',color:C.purpleLight,label:'R',size:10,bob:0,collected:false},
    ];

    const mcount = 5 + p * 2;
    for (let i = 0; i < mcount; i++) {
      const m = {...mutagens[(i+p) % mutagens.length]};
      m.x = 140 + Math.random()*(W-220); m.y = 40 + Math.random()*(H-90);
      m.vx = (Math.random()-0.5)*1.4; m.vy = (Math.random()-0.5)*1.4;
      m.angle = 0; m.hit = false;
      game.entities.push(m);
    }
    const ncount = game.nutrientsNeeded + 2;
    for (let i = 0; i < ncount; i++) {
      const n = {...nutrients[i % nutrients.length]};
      n.x = 140 + Math.random()*(W-240); n.y = 50 + Math.random()*(H-100);
      n.bob = Math.random()*Math.PI*2; n.collected = false;
      game.entities.push(n);
    }
    if (p === 3) {
      game.entities.push({type:'chromosome',x:W*0.55,y:H/2,  color:C.purple,size:18,collected:false});
      game.entities.push({type:'chromosome',x:W*0.55,y:H/2-30,color:C.purpleLight,size:18,collected:false});
    }
  }

  function generateWalls(game) {
    const W = game.canvas.width, H = game.canvas.height;
    const p = game.phase;
    game.walls = [];
    if (p===0) { game.walls.push({x:W*.32,y:H*.19,w:16,h:H*.33},{x:W*.58,y:H*.48,w:16,h:H*.33}); }
    else if (p===1) { game.walls.push({x:W*.26,y:H*.14,w:16,h:H*.38},{x:W*.51,y:H*.43,w:16,h:H*.38},{x:W*.77,y:H*.14,w:16,h:H*.33}); }
    else if (p===2) { game.walls.push({x:W*.29,y:H*.24,w:W*.28,h:16},{x:W*.57,y:H*.67,w:W*.28,h:16}); }
    else { game.walls.push({x:W*.22,y:H*.31,w:16,h:H*.38},{x:W*.51,y:H*.31,w:16,h:H*.38},{x:W*.79,y:H*.31,w:16,h:H*.38}); }
  }

  // ─── Repair ───────────────────────────────────────────────────────────────
  function tryRepair(game) {
    if (game.repairCharges > 0 && game.repairCooldown <= 0 && game.damage > 0) {
      game.repairCharges--;
      game.repairCooldown = 90;
      const healed = Math.min(game.damage, 25);
      game.damage  = Math.max(0, game.damage - healed);
      game.dna     = Math.min(100, game.dna + healed);
      addFloat(game, game.player.x, game.player.y - 20, 'Repaired!', C.sage);
      for (let i = 0; i < 6; i++) addParticle(game, game.player.x, game.player.y, C.sage);
    }
  }

  function activateP53(game) {
    game.p53Active = true; game.p53Timer = 180;
    game.repairCharges = Math.min(game.repairCharges + 2, 5);
    addFloat(game, game.canvas.width/2, game.canvas.height/2-30, 'p53 ACTIVATED!', C.sageDark);
    for (let i = 0; i < 15; i++) addParticle(game, game.canvas.width/2, game.canvas.height/2, C.sage);
  }

  // ─── Particles & floats ──────────────────────────────────────────────────
  function addParticle(game, x, y, color) {
    game.particles.push({ x, y, color, vx:(Math.random()-0.5)*4, vy:(Math.random()-0.5)*4, life:35, maxLife:35, size:3+Math.random()*3 });
  }
  function addFloat(game, x, y, text, color) {
    game.floatTexts.push({ x, y, text, color, life:60 });
  }

  // ─── Collision utils ──────────────────────────────────────────────────────
  function rectOverlap(ax,ay,aw,ah,bx,by,bw,bh){
    return ax<bx+bw&&ax+aw>bx&&ay<by+bh&&ay+ah>by;
  }

  // ─── Main update ──────────────────────────────────────────────────────────
  function update(game, uiEl, overlayEl) {
    const W = game.canvas.width, H = game.canvas.height;
    const checkpointX = W * 0.89;
    game.frameCount++;
    if (game.repairCooldown>0) game.repairCooldown--;
    if (game.p53Timer>0) game.p53Timer--; else game.p53Active=false;

    // Move player
    const speed = 6.2;
    let dx=0,dy=0;
    if (game.keys['ArrowLeft']||game.keys['KeyA']) dx-=speed;
    if (game.keys['ArrowRight']||game.keys['KeyD']) dx+=speed;
    if (game.keys['ArrowUp']||game.keys['KeyW'])   dy-=speed;
    if (game.keys['ArrowDown']||game.keys['KeyS']) dy+=speed;
    if (dx&&dy){dx*=0.707;dy*=0.707;}

    const ps=game.player.size;
    let nx=Math.max(ps,Math.min(W-ps,game.player.x+dx));
    let ny=Math.max(20,Math.min(H-20,game.player.y+dy));

    for (const w of game.walls) {
      if (rectOverlap(nx-ps,ny-ps,ps*2,ps*2,w.x,w.y,w.w,w.h)) {
        if (dx!==0&&!rectOverlap(game.player.x-ps,ny-ps,ps*2,ps*2,w.x,w.y,w.w,w.h)) nx=game.player.x;
        if (dy!==0&&!rectOverlap(nx-ps,game.player.y-ps,ps*2,ps*2,w.x,w.y,w.w,w.h)) ny=game.player.y;
      }
    }
    game.player.x=nx; game.player.y=ny;

    // Entity updates
    for (const e of game.entities) {
      if (e.type==='mutagen') {
        e.x+=e.vx; e.y+=e.vy; e.angle+=0.04;
        if (e.x<30||e.x>W-30) e.vx*=-1;
        if (e.y<30||e.y>H-30) e.vy*=-1;
        if (!e.hit&&Math.hypot(game.player.x-e.x,game.player.y-e.y)<ps+e.size) {
          e.hit=true; setTimeout(()=>{e.hit=false;},1200);
          let d=e.dmg; if(game.p53Active) d=Math.floor(d*0.4);
          game.damage=Math.min(100,game.damage+d);
          game.dna=Math.max(0,game.dna-d);
          addFloat(game,game.player.x,game.player.y-20,`-${d}`,C.terracotta);
          for(let i=0;i<5;i++) addParticle(game,e.x,e.y,e.color);
          if(game.damage>=80&&!game.p53Active) activateP53(game);
          if(game.dna<=0){ endGame(game,false,uiEl,overlayEl); return; }
          updateUI(game,uiEl);
        }
      } else if (e.type==='nutrient'&&!e.collected) {
        e.bob+=0.06;
        if(Math.hypot(game.player.x-e.x,game.player.y-e.y)<ps+e.size+4){
          e.collected=true; game.nutrients++; game.score+=10;
          if(e.label==='A'){game.damage=Math.max(0,game.damage-5);game.dna=Math.min(100,game.dna+5);}
          if(e.label==='R'&&game.repairCharges<5) game.repairCharges++;
          addFloat(game,e.x,e.y-10,e.label==='N'?'Nutrient':e.label==='A'?'Antioxidant!':'Repair+',e.color);
          for(let i=0;i<4;i++) addParticle(game,e.x,e.y,e.color);
          updateUI(game,uiEl);
        }
      } else if (e.type==='chromosome'&&!e.collected) {
        if(Math.hypot(game.player.x-e.x,game.player.y-e.y)<ps+e.size){
          e.collected=true; game.nutrients++;
          addFloat(game,e.x,e.y-15,'Chromosome!',C.purple);
          updateUI(game,uiEl);
        }
      }
    }

    if(!game.checkpointPassed&&game.nutrients>=game.nutrientsNeeded) game.checkpointOpen=true;
    if(game.checkpointOpen&&!game.checkpointPassed&&Math.hypot(game.player.x-checkpointX,game.player.y-H/2)<30){
      passCheckpoint(game,uiEl,overlayEl);
    }

    // Particles & floats
    game.particles=game.particles.filter(p=>{p.x+=p.vx;p.y+=p.vy;p.life--;p.vx*=0.9;p.vy*=0.9;return p.life>0;});
    game.floatTexts=game.floatTexts.filter(f=>{f.y-=0.7;f.life--;return f.life>0;});
  }

