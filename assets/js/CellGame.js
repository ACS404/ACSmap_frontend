  // ─── Draw ────────────────────────────────────────────────────────────────
  function draw(game) {
    const ctx=game.ctx, W=game.canvas.width, H=game.canvas.height;
    const ph=PHASES[game.phase], t=game.frameCount;
    const checkpointX=W*0.89;

    // Background
    ctx.clearRect(0,0,W,H);
    ctx.fillStyle=C.bg; ctx.fillRect(0,0,W,H);
    // Subtle dot pattern
    for(let i=0;i<10;i++){
      const x=(i*79+t*0.12)%(W+60)-30;
      const y=50+Math.sin(t*0.01+i)*70+(i*31)%200;
      ctx.beginPath(); ctx.arc(x,y,2+i%3,0,Math.PI*2);
      ctx.fillStyle=ph.color+'22'; ctx.fill();
    }

    // Walls
    for(const w of game.walls){
      ctx.fillStyle=ph.color+'22'; ctx.strokeStyle=ph.color+'55'; ctx.lineWidth=1.5;
      ctx.beginPath(); ctx.rect(w.x,w.y,w.w,w.h); ctx.fill(); ctx.stroke();
    }

    // p53 label
    if(game.p53Active){
      ctx.save(); ctx.globalAlpha=0.7+Math.sin(t*0.1)*0.3;
      ctx.fillStyle=C.sageDark; ctx.font='bold 11px Georgia,serif'; ctx.textAlign='left';
      ctx.fillText('p53 ACTIVE',10,16); ctx.restore();
    }

    // Checkpoint
    const r=22;
    ctx.save(); ctx.translate(checkpointX,H/2);
    ctx.beginPath(); ctx.arc(0,0,r,0,Math.PI*2);
    ctx.fillStyle=game.checkpointOpen?ph.color+'44':'rgba(180,160,140,0.2)';
    ctx.fill(); ctx.strokeStyle=game.checkpointOpen?ph.color:C.textMuted; ctx.lineWidth=2; ctx.stroke();
    ctx.fillStyle=game.checkpointOpen?ph.color:C.textMuted;
    ctx.font='bold 8px Georgia,serif'; ctx.textAlign='center'; ctx.textBaseline='middle';
    ctx.fillText('CHECK',0,-4); ctx.fillText('POINT',0,5);
    if(game.checkpointOpen){
      const rr=r+4+Math.sin(t*0.1)*3;
      ctx.beginPath(); ctx.arc(0,0,rr,0,Math.PI*2); ctx.strokeStyle=ph.color+'44'; ctx.lineWidth=2; ctx.stroke();
    }
    ctx.restore();
    ctx.fillStyle=game.checkpointOpen?ph.color:C.textMuted;
    ctx.font='9px Georgia,serif'; ctx.textAlign='center';
    ctx.fillText(`${game.nutrients}/${game.nutrientsNeeded}`,checkpointX,H/2+r+12);

    // Entities
    for(const e of game.entities){
      if(e.collected||e.hit===true) continue;
      ctx.save(); ctx.translate(e.x,e.y);
      if(e.type==='mutagen'){
        ctx.rotate(e.angle);
        ctx.beginPath();
        for(let i=0;i<5;i++){
          const a=i*Math.PI*2/5-Math.PI/2;
          const ro=i%2===0?e.size:e.size*0.5;
          i===0?ctx.moveTo(Math.cos(a)*ro,Math.sin(a)*ro):ctx.lineTo(Math.cos(a)*ro,Math.sin(a)*ro);
        }
        ctx.closePath(); ctx.fillStyle=e.color+'cc'; ctx.fill();
        ctx.strokeStyle=e.color; ctx.lineWidth=1.5; ctx.stroke();
        ctx.fillStyle='rgba(255,255,255,0.9)'; ctx.font='bold 7px sans-serif';
        ctx.textAlign='center'; ctx.textBaseline='middle'; ctx.fillText(e.label,0,0);
      } else if(e.type==='nutrient'){
        ctx.translate(0,Math.sin(e.bob)*3);
        ctx.beginPath(); ctx.arc(0,0,e.size,0,Math.PI*2);
        ctx.fillStyle=e.color+'99'; ctx.fill();
        ctx.strokeStyle=e.color; ctx.lineWidth=1.5; ctx.stroke();
        ctx.fillStyle='rgba(255,255,255,0.9)'; ctx.font='bold 7px sans-serif';
        ctx.textAlign='center'; ctx.textBaseline='middle'; ctx.fillText(e.label,0,0);
      } else if(e.type==='chromosome'){
        ctx.beginPath(); ctx.ellipse(0,0,e.size*0.4,e.size,0,0,Math.PI*2);
        ctx.fillStyle=e.color+'88'; ctx.fill(); ctx.strokeStyle=e.color; ctx.lineWidth=2; ctx.stroke();
      }
      ctx.restore();
    }

    // Player cell
    ctx.save(); ctx.translate(game.player.x,game.player.y);
    const pulse=1+Math.sin(t*0.08)*0.06; ctx.scale(pulse,pulse);
    if(game.p53Active){ctx.shadowColor=C.sage;ctx.shadowBlur=12;}
    ctx.beginPath(); ctx.arc(0,0,game.player.size,0,Math.PI*2);
    ctx.fillStyle=ph.color; ctx.fill();
    ctx.strokeStyle='rgba(255,255,255,0.5)'; ctx.lineWidth=2; ctx.stroke();
    ctx.beginPath(); ctx.arc(0,0,game.player.size*0.45,0,Math.PI*2);
    ctx.fillStyle='rgba(255,255,255,0.25)'; ctx.fill();
    if(game.repairCooldown>0){
      ctx.beginPath();
      ctx.arc(0,0,game.player.size+4,Math.PI*1.5,Math.PI*1.5+Math.PI*2*(1-game.repairCooldown/90));
      ctx.strokeStyle='rgba(255,255,255,0.6)'; ctx.lineWidth=2; ctx.stroke();
    }
    ctx.restore();

    // Particles
    for(const p of game.particles){
      ctx.globalAlpha=p.life/p.maxLife; ctx.fillStyle=p.color;
      ctx.beginPath(); ctx.arc(p.x,p.y,p.size*(p.life/p.maxLife),0,Math.PI*2); ctx.fill();
    }
    ctx.globalAlpha=1;

    // Float texts
    for(const f of game.floatTexts){
      ctx.globalAlpha=f.life/60; ctx.fillStyle=f.color;
      ctx.font='bold 10px Georgia,serif'; ctx.textAlign='center';
      ctx.fillText(f.text,f.x,f.y);
    }
    ctx.globalAlpha=1;

    // HUD
    const phLabel=PHASES[game.phase];
    ctx.fillStyle=phLabel.color; ctx.font='bold 11px Georgia,serif';
    ctx.textAlign='center'; ctx.fillText(phLabel.full,W/2,14);
    ctx.fillStyle=C.textMuted; ctx.font='9px Georgia,serif';
    ctx.textAlign='left'; ctx.fillText(`Score: ${game.score}`,8,H-6);
    ctx.textAlign='right';
    const charges='■'.repeat(game.repairCharges)+'□'.repeat(Math.max(0,3-game.repairCharges));
    ctx.fillText('SPACE repair: '+charges,W-8,H-6);

    // Phase bar
    const bw=(W-32)/4;
    for(let i=0;i<4;i++){
      ctx.fillStyle=i<game.phase?PHASES[i].color+'77':i===game.phase?PHASES[i].color+'33':'rgba(180,160,140,0.15)';
      ctx.fillRect(16+i*bw,H-22,bw-3,8);
      if(i===game.phase){ctx.strokeStyle=PHASES[i].color;ctx.lineWidth=1.5;ctx.strokeRect(16+i*bw,H-22,bw-3,8);}
      ctx.fillStyle=i===game.phase?PHASES[i].color:C.textMuted;
      ctx.font=i===game.phase?'bold 7px sans-serif':'7px sans-serif';
      ctx.textAlign='center';
      ctx.fillText(PHASES[i].name,16+i*bw+bw/2-1.5,H-15);
    }

    // Legend (top-right)
    const legend=[
      {c:C.sage,  l:'N Nutrient'},
      {c:C.tealGame,l:'A Antioxidant'},
      {c:C.purpleLight,l:'R Repair'},
      {c:C.orange,l:'UV Mutagen'},
      {c:C.terracotta,l:'C Carcinogen'},
      {c:C.rose,  l:'V Virus/HPV'},
    ];
    ctx.font='8px sans-serif'; ctx.textAlign='left';
    let lx=W-130,ly=26;
    for(const it of legend){
      ctx.fillStyle=it.c; ctx.beginPath(); ctx.arc(lx,ly,4,0,Math.PI*2); ctx.fill();
      ctx.fillStyle=C.text+'bb'; ctx.fillText(it.l,lx+8,ly+3); ly+=13;
    }
  }

  // ─── Game loop ────────────────────────────────────────────────────────────
  function loop(game, uiEl, overlayEl) {
    if(game.animId) cancelAnimationFrame(game.animId);
    const tick = () => {
      if(!game.running) return;
      update(game, uiEl, overlayEl);
      draw(game);
      game.animId = requestAnimationFrame(tick);
    };
    tick();
  }

